let hari = parseInt(prompt("Masukkan nomor hari (1-7): "));

if (hari === 1) {
    console.log("SENIN");
} else if (hari === 2) {
    console.log("SELASA");
} else if (hari === 3) {
    console.log("RABU");
} else if (hari === 4) {
    console.log("KAMIS");
} else if (hari === 5) {
    console.log("JUMAT");
} else if (hari === 6) {
    console.log("SABTU");
} else if (hari === 7) {
    console.log("MINGGU");
} else {
    console.log("Nomor hari tidak valid.");
}